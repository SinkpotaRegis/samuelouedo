            <p class="font-italic">
              Un Samuel, ou servant d'autel, ou servant de messe, est un jeune enfant qui sert le seigneur en présence du prêtre à l'autel pendant les célébrations liturgiques, surtout la messe, dans une tenue culturelle nommée " Aube " et veille sur les mobiliers liturgiques.<br>
             <br><hr> Le rôle du servant de messe est d'aider le prêtre à célébrer la liturgie, en lui apportant les mobiliers nécessaires, en participant aux processions. Il contribue ainsi à la beauté de la messe. Il est aussi un exemple pour l'assemblée, par son attitude recueillie, sa participation active et sa prière fervente. Il approfondit sa foi, sa connaissance et son amour du Christ, et témoigne de sa présence dans le monde.
