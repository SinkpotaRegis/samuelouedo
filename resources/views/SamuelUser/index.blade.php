            <p>
              Le vicariat forain Saint Luc de Ouèdo est l'un des vicariats de l'archidiocèse de Cotonou. Il est né de la fusion de deux anciens doyennés : Togoudo et Hêvié. Niché au cœur de la commune d'Abomey-Calavi, dans le département de l'Atlantique, ce vicariat représente un véritable creuset de foi et d'unité. Il abrite diverses communautés, parmi lesquelles se trouve la communauté vicariale des SAMUEL, rassemblant les servants de messe du vicariat.

              <br>Avec ses 17 paroisses actives, la communauté vicariale des SAMUEL est le reflet d'une vie communautaire vibrante, imprégnée de valeurs fondamentales telles que la paix, la compréhension, l'amour du prochain, la solidarité et l'innovation. Cette synergie témoigne de l'engagement des membres de cette humble communauté envers une existence harmonieuse, orientée vers le bien-être collectif.

              <br>Au-delà de son succès actuel, la communauté vicariale des SAMUEL puise sa force dans l'héritage laissé par ses aînés, qui ont contribué à façonner son destin. Les valeurs de la communauté prennent racine dans la foi en l'œuvre divine et s'épanouissent sous la bienveillance protectrice du Saint-Esprit.

              <br>À mesure que la communauté progresse, elle reste ancrée dans un processus continu de croissance et de formation, illustrant ainsi la vitalité de son parcours spirituel. La communauté vicariale des SAMUEL du vicariat forain Saint Luc de Ouèdo incarne une expression vivante de la foi chrétienne, nourrie par le passé, animée par le présent et guidée vers un avenir empreint de spiritualité.
